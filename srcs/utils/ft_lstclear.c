#include "utils.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*cur;

	while (*lst)
	{
		cur = *lst;
		*lst = cur->next;
		ft_lstdelone(cur, del);
	}
}
